=encoding UTF-8

=head1 NAME

Protobuf::Message - Base class for Protocol Buffer messages

=head1 VERSION

version 0.05

=head1 SYNOPSIS

    # This class is not used directly. Instead, use classes generated
    # from your .proto files.

    # Example assuming MyPackage::MyMessage is generated:
    # use MyPackage::MyMessage;

    # my $msg = MyPackage::MyMessage->new(field1 => 'value1');
    # $msg->field2(123);

    # my $binary = $msg->serialize;
    # my $decoded = MyPackage::MyMessage->parse($binary);

    # is($decoded->field1, 'value1', 'Field 1 matches');

=head1 DESCRIPTION

C<Protobuf::Message> is the base class for all Perl classes generated from Protocol Buffer message definitions. It provides the core functionality for creating, manipulating, serializing, and deserializing messages.

You should not use this class directly. Instead, compile your C<.proto> files using C<protoc> with the C<protoc-gen-perl-pb> plugin, which will generate subclasses of C<Protobuf::Message> with methods specific to your message fields.

The underlying implementation uses the C<upb> library, and most method calls are dispatched to highly optimized C functions via L<XS>.

=head1 ATTRIBUTES

Message classes generated by the plugin will have attributes corresponding to the fields defined in the C<.proto> file. These are not defined directly in this base class but are injected by L<Protobuf::ClassGenerator> based on the message descriptor.

=head1 METHODS

== Class Methods

=head2 new(%args)

    my $msg = MyGeneratedMessage->new(
        field_a => 'hello',
        nested_msg => { inner_field => 123 },
        repeated_field => [1, 2, 3],
    );

Creates a new message instance. Fields can be initialized using key-value pairs.

This method cannot be called on C<Protobuf::Message> directly; it must be called on a generated subclass.

=head2 parse($binary_data)

    my $msg = MyGeneratedMessage->parse($binary_string);

Parses a binary string (wire format) and returns a new message instance.

=head2 parse_from($binary_data)

    $msg->parse_from($binary_string);

Parses a binary string and merges its fields into the existing message instance.

=head2 merge_from($other_msg)

    $msg->merge_from($other_msg);

Deep-merges the fields from C<$other_msg> into the current message. C<$other_msg> must be of the same type.

=head2 copy_from($other_msg)

    $msg->copy_from($other_msg);

Deep-copies the fields from C<$other_msg> into the current message, clearing any existing fields first.

=head2 from_json($json_string)

    my $msg = MyGeneratedMessage->from_json($json_string);

Parses a JSON string representing the message and returns a new message instance.

=head2 descriptor()

    my $mdef = MyGeneratedMessage->descriptor;
    print $mdef->full_name;

Returns the L<Protobuf::Descriptor::MessageDef> object for this message type.

== Instance Methods

Most field-specific methods (getters, setters, etc.) are dynamically generated in subclasses. The methods listed below are common to all messages.

=head2 get($field_name)

Generic accessor to get a field's value. Prefer using the field-specific methods in generated classes (e.g., C<< $msg->my_field >>).

=head2 set($field_name, $value)

Generic accessor to set a field's value. Prefer using the field-specific methods in generated classes (e.g., C<< $msg->my_field('new_value') >>).

=head2 has_field($field_name)

Returns true if the specified singular field is set. Prefer C<< $msg->has_my_field >>.

=head2 clear_field($field_name)

Clears the specified field. Prefer C<< $msg->clear_my_field >>.

=head2 serialize()

    my $binary_string = $msg->serialize;

Serializes the message to the binary wire format.

=head2 to_text()

    my $text_format = $msg->to_text;

Converts the message to the Protocol Buffer TextFormat. Useful for debugging.

=head2 to_json()

    my $json_string = $msg->to_json;

Converts the message to its JSON representation.

=head2 unknown_fields()

Returns a L<Protobuf::UnknownFieldSet> object representing any fields encountered during parsing that were not defined in the message's descriptor.

=head2 fields()

    my @fields = $msg->fields;

Returns a list of L<Protobuf::Descriptor::Field> objects for all fields that are currently set in the message.

=head2 CLONE()

Throws an error. Protobuf messages cannot be directly cloned, especially across threads, due to the underlying C structures and arena management.

=head2 DESTROY()

Internal method to clean up the underlying C<upb_Message> and related resources.

=head1 ADVANCED METHODS

These methods are for more specialized use cases.

=head2 set_oneof($oneof_name, $value)

Sets a value within a C<oneof> group. This method attempts to coerce the given C<$value> into the first field within the named C<oneof> group that can accept it. Returns the name of the field that was set.

=head2 which_oneof($oneof_name)

Returns the name of the field that is currently set within the specified C<oneof> group, or C<undef> if no field is set.

=head2 audit_integrity()

Internal method for debugging memory and object cache integrity.

=head2 get_fingerprint()

Returns a unique fingerprint string for the message instance, often related to its memory location in the arena. Useful for debugging and tracking object identity in shared memory scenarios.

=head2 freeze_to_shared($path, $size)

(Experimental) Serializes and copies the message into a shared memory segment (C<tmpfs>) at the given C<$path>, with a maximum C<$size>. Returns a new instance backed by the shared arena.

=head2 thaw_from_shared($class, $path, $size)

(Experimental) Attaches to an existing shared memory segment at C<$path> and attempts to reify a message of type C<$class> from it.

=head2 to_perl()

Converts the message to a Perl hash/array structure. L<Protobuf::WKT::Struct> has a specialized version.

=head2 from_perl($hashref)

Populates the message fields from a Perl hashref.

=head2 validate()

(Placeholder) Validates the message content based on any defined constraints.

=head2 coerce_to($target_class)

(Placeholder) Attempts to coerce the current message to an instance of C<$target_class>.

=head2 to_text_with_unknowns()

(Placeholder) Converts the message to TextFormat, including a representation of any unknown fields. Currently alias to C<to_text()>.

=head2 to_wire()

Alias for C<serialize()>. Returns the binary wire format.

=head2 to_json_streaming()

(Placeholder) Returns a JSON representation of the message, potentially optimized for streaming. Currently alias to C<to_json()>.

=head2 to_json_compact()

Returns a JSON representation of the message without extra whitespace.

=head2 dependency_graph()

(Placeholder) Returns a structure representing the dependencies of this message type.

=head2 perf_profile()

(Placeholder) Returns a hash containing performance metrics related to this message.

=head2 reset_connection()

(Placeholder) Resets any connection-related state within the message object.

=head1 MEMORY MANAGEMENT

C<Protobuf::Message> instances are typically allocated on a L<Protobuf::Arena>. The arena manages the memory for the message and all its submessages. When the arena is freed (usually when the top-level message object goes out of scope), all associated memory is reclaimed.

