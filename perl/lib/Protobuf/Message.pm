sub coerce_to {
    my ($self, $target_class) = @_;
    return _xs_coerce_to($self, $target_class);
}

sub to_perl {
    my ($self) = @_;
    return $self->_engine->to_perl($self);
}

sub to_hashref {
    my ($self) = @_;
    return $self->to_perl();
}

sub from_perl {
    my ($self, $data) = @_;
    croak('Message::from_perl expects a HASH ref') unless ref($data) eq 'HASH';
    foreach my $key (keys %$data) {
        if ($self->can($key)) {
            $self->$key($data->{$key});
        } else {
            croak("No such field or method: $key");
        }
    }
    return $self;
}

sub to_text {
    my ($self) = @_;
    return $self->_engine->to_text($self);
}

sub to_text_with_unknowns {
    my ($self) = @_;
    return $self->to_text();
}

sub to_wire {
    my ($self) = @_;
    return $self->serialize();
}

sub to_json {
    my ($self) = @_;
    return $self->_engine->to_json($self);
}

sub toJSON { shift->to_json(@_) }

sub to_handle {
    my ($self, $fh, %options) = @_;
    croak("Invalid file handle") unless defined $fh;
    # TODO: Engine delegation for to_handle
    if (($options{format} || 'binary') eq 'json') {
        return _xs_json_to_handle($self, $fh);
    }
    return _xs_to_handle($self, $fh, $options{length_prefixed} || 0);
}

sub from_handle {
    my ($class, $fh, %options) = @_;
    croak("Invalid file handle") unless defined $fh;
    # TODO: Engine delegation for from_handle
    return _xs_from_handle($class, $fh, $options{length_prefixed} || 0);
}

sub to_json_streaming {
    my ($self) = @_;
    return $self->to_json();
}

sub to_json_compact {
    my ($self) = @_;
    my $json = $self->to_json();
    $json =~ s/\s+//g;
    return $json;
}

sub from_json {
    my ($class, $json_data, $options) = @_;
    return $class->_engine->from_json($class, $json_data, $options);
}

sub fromJSON { shift->from_json(@_) }

sub unknown_fields {
    my ($self) = @_;
    if ($self->_engine->isa('Protobuf::Engine::XS')) {
        return _xs_unknown_fields($self);
    } else {
        require Protobuf::UnknownFieldSet;
        return bless { _msg => $self }, 'Protobuf::UnknownFieldSet';
    }
}

sub dependency_graph {
    my ($self) = @_;
    return { root => ref($self), children => [] };
}

sub perf_profile {
    my ($self) = @_;
    return { serialize_time => 0, deserialize_time => 0, field_access_avg => 0 };
}

sub reset_connection {
    my ($self) = @_;
    return 1;
}

sub parse {
    my ($class, $data, $options) = @_;
    my $profile = (ref($options) eq 'HASH') ? $options->{profile} : undef;
    my $engine = $class->_engine($profile);
    my $self = $engine->parse($class, $data, $options);
    $self->{_engine} = $engine;
    return $self;
}

sub decode { shift->parse(@_) }

sub parse_from {
    my ($self, $data) = @_;
    if ($self->_engine->isa('Protobuf::Engine::XS')) {
        _xs_clear_memoization_cache($self);
        return _xs_parse_from($self, $data);
    } else {
        return $self->_engine->parse_into($self, $data);
    }
}

sub merge_from {
    my ($self, $other) = @_;
    croak("Argument to merge_from must be a Protobuf::Message")
        unless eval { $other->isa('Protobuf::Message') };
    _xs_clear_memoization_cache($self) if $self->_engine->isa('Protobuf::Engine::XS');
    return $self->_engine->merge($self, $other);
}

sub copy_from {
    my ($self, $other) = @_;
    croak("Argument to copy_from must be a Protobuf::Message")
        unless eval { $other->isa('Protobuf::Message') };
    _xs_clear_memoization_cache($self) if $self->_engine->isa('Protobuf::Engine::XS');
    return $self->_engine->copy($self, $other);
}

sub fields {
    my ($self) = @_;
    if ($self->_engine->isa('Protobuf::Engine::XS')) {
        my $fields_rv = _xs_fields($self);
        return wantarray ? @$fields_rv : $fields_rv;
    } else {
        my $mdef = $self->{_mdef};
        my $fields = $self->{_fields};
        my @res;
        foreach my $name (keys %$fields) {
            my $fdef = $mdef->find_field_by_name($name);
            push @res, $fdef if $fdef;
        }
        return wantarray ? @res : \@res;
    }
}

sub freeze_to_shared {
    my ($self, $path, $size) = @_;
    $size ||= 1024 * 1024;
    my $arena = Protobuf::Arena->new_tmpfs($path, $size);
    my $shared_msg = _xs_migrate_to_arena($self, $arena);
    return $shared_msg;
}

sub thaw_from_shared {
    my ($class, $path, $size) = @_;
    $size ||= 1024 * 1024;
    my $arena = Protobuf::Arena->attach_tmpfs($path, $size);
    return _xs_find_in_shared_arena($class, $arena);
}

sub detach {
    my $self = shift;
    return _xs_detach($self);
}

sub attach {
    my ($class, $info, $arena) = @_;
    return _xs_attach($class, $info, $arena);
}

sub arena {
    my $self = shift;
    return _xs_get_arena($self);
}

sub FREEZE {
    my ($self) = @_;
    $self->{'_serialized'} = $self->serialize();
    return;
}

sub THAW {
    my ($self) = @_;
    my $serialized = delete $self->{'_serialized'};
    if (defined $serialized) {
        my $class = ref($self);
        my $fresh = $class->parse($serialized);
        %{$self} = ();
        while (my ($k, $v) = each %{$fresh}) {
            $self->{$k} = $v;
        }
    }
    return $self;
}

1;
