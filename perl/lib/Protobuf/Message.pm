package Protobuf::Message;

use strict;
require Protobuf;
use warnings;
use Carp qw(croak);

our $VERSION = '0.05';

use Protobuf::Internal::Repeated;
use Protobuf::Internal::Map;

sub _engine {
    my ($class_or_self, $profile) = @_;
    if (ref($class_or_self)) {
        return $class_or_self->{_engine} if $class_or_self->{_engine};
    }
    my $name = ($profile && $profile eq 'pure_perl') ? 'pure_perl' : undef;
    return Protobuf->get_engine($name);
}

sub new {
    my $class = shift;
    my $args;
    if (@_ == 1 && ref($_[0]) eq 'HASH') {
        $args = $_[0];
    } else {
        $args = { @_ };
    }

    croak("Protobuf::Message->new cannot be called directly. Use a generated subclass.")
        if $class eq 'Protobuf::Message';

    my $mdef = $class->descriptor;
    croak("Class $class does not have a descriptor") unless $mdef;

    my $profile_str = delete $args->{profile} || 'balanced';
    my $engine = $class->_engine($profile_str);

    my $flags = 0;
    if ($profile_str eq 'write_heavy') {
        $flags = Protobuf::Internal::PROFILE_WRITE_HEAVY();
    } elsif ($profile_str eq 'read_heavy') {
        $flags = Protobuf::Internal::PROFILE_READ_HEAVY();
    } elsif ($profile_str eq 'zero_copy') {
        $flags = Protobuf::Internal::PROFILE_ZERO_COPY();
    }

    my $self = $engine->create_message($class, $mdef, delete $args->{arena}, $flags);
    $self->{_engine} = $engine;

    $self->from_perl($args) if keys %$args;

    return $self;
}

sub DESTROY {
    my ($self) = @_;
    return if ${^GLOBAL_PHASE} eq 'DESTRUCT';
    # Only call XS free if it's an XS-backed object
    if (exists $self->{_engine} && defined $self->{_engine} && $self->{_engine}->isa('Protobuf::Engine::XS')) {
        _xs_free($self);
    }
    return;
}
sub CLONE_SKIP { 1 }

sub get {
    my ($self, $field_name) = @_;
    return $self->_engine->get($self, $field_name);
}

sub set {
    my ($self, $field_name, $value) = @_;
    return $self->_engine->set($self, $field_name, $value);
}

sub has {
    my ($self, $field_name) = @_;
    return $self->_engine->has($self, $field_name);
}

sub clear {
    my ($self, $field_name) = @_;
    return $self->_engine->clear($self, $field_name);
}

sub set_oneof {
    my ($self, $oneof_name, $value) = @_;
    my $mdef = $self->descriptor;
    my $oneof = $mdef->find_oneof_by_name($oneof_name);
    croak("Oneof '$oneof_name' not found") unless $oneof;

    my %type_priority = (
        11 => 1, # MESSAGE
        1  => 2, 2 => 2, 3 => 2, 4 => 2, 5 => 2, 6 => 2, 7 => 2, 13 => 2, 15 => 2, 16 => 2, 17 => 2, 18 => 2, # Numeric
        14 => 3, # ENUM
        8  => 4, # BOOL
        9  => 5, 12 => 5, # STRING, BYTES
    );

    my @sorted_fields = sort {
        my $p_a = $type_priority{$a->type_number} || 6;
        my $p_b = $type_priority{$b->type_number} || 6;
        $p_a <=> $p_b;
    } $oneof->fields;

    foreach my $f (@sorted_fields) {
        my $method = $f->name;
        eval {
            $self->$method($value);
            return 1;
        };
        if (!$@) {
            return $f->name; # Successfully set
        }
    }
    croak("Could not coerce value into any field of oneof '$oneof_name'");
    return;
}

sub has_field {
    my ($self, $field_name) = @_;
    if ($self->_engine->isa('Protobuf::Engine::XS')) {
        return _xs_has($self, $field_name);
    } else {
        return $self->has($field_name);
    }
}

sub clear_field {
    my ($self, $field_name) = @_;
    if ($self->_engine->isa('Protobuf::Engine::XS')) {
        return _xs_clear($self, $field_name);
    } else {
        return $self->clear($field_name);
    }
}

sub which_oneof {
    my ($self, $oneof_name) = @_;
    if ($self->_engine->isa('Protobuf::Engine::XS')) {
        return _xs_which_oneof($self, $oneof_name);
    } else {
        my $mdef = $self->descriptor;
        my $oneof = $mdef->find_oneof_by_name($oneof_name);
        croak("Oneof '$oneof_name' not found") unless $oneof;

        foreach my $f ($oneof->fields) {
            if ($self->has($f->name)) {
                return $f->name;
            }
        }
        return undef;
    }
}

sub serialize {
    my ($self) = @_;
    return $self->_engine->serialize($self);
}

sub encode { shift->serialize(@_) }

